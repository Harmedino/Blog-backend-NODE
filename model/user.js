const mongoose = require("mongoose");
const blogSchema = require("./blog");
const { Schema } = mongoose;

const userSchema = new Schema(
  {
    firstname: {
      type: String,
      required: true,
    },
    lastname: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
    },
    role: {
      type: String,
      default: "User",
    },
    blog: [blogSchema],
  },
  { timestamps: true }
);

module.exports = mongoose.model("User", userSchema);
